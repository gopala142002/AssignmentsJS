"use strict"
//celsius to fahrenheit
var inCal1=60;
var infahr1=(9*inCal1)/5+32;
console.log(`${inCal1} degree celsius = ${infahr1} degree fahrenheit`);
//fahrenheit to celsius
var infahr2=45;
var inCal2=(5/9)*(infahr2-32);
console.log(`${infahr2} degree fahrenheit = ${inCal2} degree celsius`);